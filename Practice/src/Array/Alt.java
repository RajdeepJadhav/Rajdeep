package Array;
//WAP to print the alternate values from an array.
public class Alt {

	public static void main(String[] args) {
			int a[]= {1,2,3,4,5,6};
			int n=a.length/2;
			for(int s:a) 
			{
				if(s>n) 
				{
					System.out.println(s);
				}
				else
					System.out.println(s);
			}

	}

}
